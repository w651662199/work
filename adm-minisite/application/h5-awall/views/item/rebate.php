    <dl class="main-dl">
        <dd class="dd-question"><span>Q</span>什么是“分享返利”？</dd>
        <dd class="dd-answer"><span>A</span> “分享返利”是国美Plus联合优质商家鼓励用户分享高品质商品的用户回馈活动。动动手指就能把国美Plus上的好东西、好内容分享给好友，还有丰厚返利等你来拿。</dd>
        <dd class="dd-question"><span>Q</span>怎样才能成功“分享返利”？</dd>
        <dd class="dd-answer"><span>A</span>发现频道中每个带有“分享可得”标识的内容都有一定的国美币返利金额（没有标记的一律为0）。登录状态下通过国美Plus客户端将内容分享给好友后，好友点开你的“分享返利”链接，系统会根据每个内容页面上所显示的返利金额为你计算并尽快发放返利至国美币账户。</dd>
        <dd class="dd-question"><span>Q</span>为什么我的朋友打开了我分享的链接，我没有获得返利？</dd>
        <dd class="dd-answer"><span>A</span>主要可能有以下原因：
        <dd class="dd-answer-list">1. 好友打开你的分享链接时该内容的返利活动已截止，考验小伙伴们手速的时候到啦；</dd>
        <dd class="dd-answer-list">2. 每个返利活动内容在单个自然日内最多只能获得一次返利；</dd>
        <dd class="dd-answer-list">3. 在非国美Plus客户端环境下进行返利活动内容分享；</dd>
        <dd class="dd-answer-list">4. 协议中规定的其他情况。</dd>
        </dd>
        <dd class="dd-question"><span>Q</span>返利获得的国美币有什么用？</dd>
        <dd class="dd-answer"><span>A</span>国美币可在国美Plus平台上购买商品，在填写订单页面勾选即可使用。同时可通过绑定银行卡（储蓄卡）进行提现。</dd>
    </dl>
